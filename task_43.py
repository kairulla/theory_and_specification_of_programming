# Задача 43. Определить сколько букв «а» есть в фамилии.

# Введите фамилию
surname = input("Введите фамилию: ")

# Подсчитываем количество букв 'а' (и 'А') в фамилии
a_count = 0
for char in surname:
    if char.lower() == 'а':
        a_count += 1

# Выводим результат
print(f"Количество букв 'а' в фамилии: {a_count}")
