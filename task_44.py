# Задача 44.
# Пусть задана ваша фамилия имя и отчество.
# Определить длину строки и вывести на экран имя.

# Задайте вашу фамилию, имя и отчество
full_name = "Фамилия Имя Отчество"  # Замените на свои данные

# Определите длину строки
length = len(full_name)

# Разделите полное имя на компоненты (фамилия, имя, отчество)
name_parts = full_name.split()

# Выведите имя (второй компонент)
if len(name_parts) >= 2:
    first_name = name_parts[1]
    print(f"Длина строки: {length}")
    print(f"Имя: {first_name}")
else:
    print("Ошибка: Не удалось определить имя. Убедитесь, что вы ввели фамилию, имя и отчество.")
