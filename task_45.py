# Задача 45.
# Пусть задана ваша фамилия имя и отчество.
# Определить количество букв в фамилии.

# Задайте вашу фамилию, имя и отчество
full_name = "Фамилия Имя Отчество"  # Замените на свои данные

# Разделите полное имя на компоненты (фамилия, имя, отчество)
name_parts = full_name.split()

# Инициализируйте счетчик букв в фамилии
letters_in_surname = 0

# Подсчитайте количество букв в фамилии (первом компоненте)
if len(name_parts) >= 1:
    surname = name_parts[0]
    letters_in_surname = len(surname)

# Выведите количество букв в фамилии
print(f"Количество букв в фамилии: {letters_in_surname}")
