# Пусть задана ваша фамилия имя и отчество.
# Вывести на экран инициалы с точками.

# Задайте вашу фамилию, имя и отчество
full_name = "Фамилия Имя Отчество"  # Замените на свои данные

# Разделите полное имя на компоненты (фамилия, имя, отчество)
name_parts = full_name.split()

# Инициализируйте переменные для инициалов
initials = ""

# Подсчитайте инициалы (первая буква каждого компонента)
for part in name_parts:
    initials += part[0].upper() + "."  # Преобразуйте первую букву в верхний регистр и добавьте точку

# Выведите инициалы на экран
print(f"Инициалы: {initials}")
